import { createFileRoute, useNavigate } from "@tanstack/react-router";
import { useEffect, useState } from "react";
import { Sparkles, Rocket, Bot, Loader2, Check, Play, Send, ArrowRight, BriefcaseBusiness, MessageSquareMore, Code2, Image as ImageIcon, Gamepad2, Wand2 } from "lucide-react";
import { useStudioContext } from "@/context/StudioContext";

export const Route = createFileRoute("/_app/create")({
  head: () => ({
    meta: [
      { title: "Create — Creator Studio" },
      { name: "description", content: "Describe your game in a prompt and let the agent build a playable version." },
    ],
  }),
  component: Create,
});

const ideas: Array<{ icon: string; text: string }> = [
  { icon: "🏙️", text: "A neon-cyberpunk parkour runner in Tokyo" },
  { icon: "🐾", text: "Cozy farm sim where animals talk back" },
  { icon: "🪐", text: "Voxel space pirate adventure" },
  { icon: "🐱", text: "Roguelike dungeon crawler with cats" },
  { icon: "🥋", text: "Tekken style martial arts fighter generated by the AI agent" },
];

const vibeIdeas = ["Chill vibes", "World Cup energy", "Neon arcade", "Kids friendly"];

const steps = ["Reading prompt", "Designing mechanics", "Generating assets", "Compiling playable build"];

type ChatMessage = {
  role: "assistant" | "user";
  text: string;
};

type ChatStage = "game" | "vibe" | "concept" | "ready";

const conceptByGame: Record<string, string[]> = {
  chess: [
    "Royal Gambit: A calm 1-on-1 chess match against a thoughtful AI on a clean neon board.",
    "Knight's Duel: Sharp tactical chess with highlighted legal moves and quick AI replies.",
  ],
  soccer: [
    "World Cup Rush: A fast arcade soccer match with quick goals, dramatic saves, and bright stadium energy.",
    "Street Header: A playful head-soccer duel in a neon street arena with bouncy physics.",
  ],
  runner: [
    "River Ripple: Guide a calm lily pad through a winding river, collecting blossoms and avoiding leaves.",
    "Dream Dash: Run through a soft pastel skyline, collecting stars and gliding past gentle obstacles.",
  ],
  racing: [
    "Mini Traffic Drift: Weave through traffic, collect boost pickups, and chase the cleanest lap.",
    "Sunset Sprint: A chill top-down racer with warm roads, smooth turns, and simple mobile controls.",
  ],
  bubble: [
    "Bubble Reef: Aim colorful bubbles through a peaceful underwater board with satisfying pops.",
    "Cloud Popper: Clear floating bubble clusters in a soft sky world.",
  ],
  match: [
    "Block Bloom: Swap colorful blocks to grow combos in a cozy puzzle garden.",
    "Gem Picnic: A bright match-3 board with relaxed scoring and cheerful effects.",
  ],
};

function gameKind(text: string) {
  const value = text.toLowerCase();
  if (/(chess|checkmate|chessboard)/.test(value)) return "chess";
  if (/(soccer|football|world cup)/.test(value)) return "soccer";
  if (/(runner|running|run|parkour|jump)/.test(value)) return "runner";
  if (/(racing|racer|race|car|driv)/.test(value)) return "racing";
  if (/(bubble|shooter|aim)/.test(value)) return "bubble";
  if (/(match|blocks|jewel|candy)/.test(value)) return "match";
  return "runner";
}

function conceptFor(game: string, vibe: string) {
  const [first, fallback] = conceptByGame[game] ?? conceptByGame.runner;
  if (/chill|calm|cozy|peace|soft|relax/i.test(vibe)) return first;
  return fallback ?? first;
}

function Create() {
  const { studio, addCreatedGame, removeCreatedGame } = useStudioContext();
  const navigate = useNavigate();
  const [chatStage, setChatStage] = useState<ChatStage>("game");
  const [chatInput, setChatInput] = useState("");
  const [gameRequest, setGameRequest] = useState("");
  const [vibeRequest, setVibeRequest] = useState("");
  const [selectedConcept, setSelectedConcept] = useState("");
  const [messages, setMessages] = useState<ChatMessage[]>([
    { role: "assistant", text: "Hey there! What kind of game do you want to create?" },
  ]);

  // The build itself lives in studio state + localStorage (kult-active-build),
  // so it survives navigating away and full page refreshes. This page only
  // renders whatever build is active until the user cancels/dismisses it.
  const activeBuild = studio.activeBuild;
  const phase: "idle" | "building" | "done" | "failed" = activeBuild ? activeBuild.phase : "idle";
  const buildingStrategy = phase === "building" ? activeBuild!.strategy : null;
  const builtGame = activeBuild?.game ?? null;
  // Re-render once a second while building so the elapsed time and step list move
  // even between 5s job polls.
  const [, setTick] = useState(0);
  useEffect(() => {
    if (phase !== "building") return;
    const interval = setInterval(() => setTick((t) => t + 1), 1000);
    return () => clearInterval(interval);
  }, [phase]);
  const elapsedSec = activeBuild ? Math.max(0, Math.floor((Date.now() - activeBuild.startedAt) / 1000)) : 0;
  const step =
    phase === "building" ? Math.min(steps.length - 1, Math.floor(elapsedSec / 2)) : steps.length - 1;

  const build = async (strategy: "pure-agent" | "hybrid", promptOverride = "") => {
    const buildPrompt = promptOverride || studio.prompt;
    if (!buildPrompt.trim() || phase === "building") return;
    const game = await studio.generateFromPrompt(strategy, buildPrompt);
    addCreatedGame(game ?? studio.generatedPackage);
  };

  // Cancel a running build: stop polling, drop the persisted record, and delete
  // the already-saved game entry so nothing half-built lingers in My Creations.
  const cancelBuild = () => {
    const gameId = activeBuild?.game?.id;
    studio.cancelActiveBuild();
    if (phase === "building" && gameId) void removeCreatedGame(gameId);
  };

  const sendChat = (text = chatInput) => {
    const value = text.trim();
    if (!value || phase === "building") return;
    setChatInput("");

    if (chatStage === "game") {
      setGameRequest(value);
      setChatStage("vibe");
      setMessages((current) => [
        ...current,
        { role: "user", text: value },
        {
          role: "assistant",
          text: `Awesome! ${value} games are super fun. Do you have any specific vibes in mind, or should I throw out some ideas?`,
        },
      ]);
      return;
    }

    if (chatStage === "vibe") {
      const game = gameKind(gameRequest || value);
      const concept = conceptFor(game, value);
      setVibeRequest(value);
      setSelectedConcept(concept);
      setChatStage("concept");
      setMessages((current) => [
        ...current,
        { role: "user", text: value },
        { role: "assistant", text: `Perfect choice. Here is a ${value} idea:` },
        { role: "assistant", text: concept },
        {
          role: "assistant",
          text: "Say “Ok, create it!” when you want me to build this from the closest playable template.",
        },
      ]);
      return;
    }

    if (chatStage === "concept") {
      const prompt = `${gameRequest}. ${vibeRequest}. ${selectedConcept}.`;
      studio.setPrompt(prompt);
      setChatStage("ready");
      setMessages((current) => [
        ...current,
        { role: "user", text: value },
        { role: "assistant", text: `I've created a plan for you. Building ${selectedConcept.split(":")[0]} from a matching template now.` },
      ]);
      void build("hybrid", prompt);
    }
  };

  const freshChat = chatStage === "game" && messages.length <= 1;

  return (
    <div>
      {/* Hero */}
      <div className="relative overflow-hidden border-b border-border/40">
        <div className="pointer-events-none absolute -right-24 -top-24 size-96 rounded-full bg-[oklch(0.65_0.25_295)]/15 blur-3xl" />
        <div className="mx-auto flex max-w-6xl items-center justify-between gap-8 px-4 py-4 sm:px-6 lg:px-10 [@media(max-height:780px)]:py-2.5">
          <div className="min-w-0">
            <h1 className="flex items-start gap-2 font-display text-3xl font-black tracking-tight sm:text-4xl">
              Create
              <Sparkles className="mt-0.5 size-5 text-primary" />
            </h1>
            <p className="label-mono mt-2 text-[10px] tracking-[0.18em] text-muted-foreground">
              PROMPT <span className="text-muted-foreground/60">→</span>{" "}
              <span className="text-primary">PLAYABLE</span>{" "}
              <span className="text-muted-foreground/60">→</span> DESCRIBE IT, THE AGENT BUILDS IT
            </p>
            <p className="mt-2 hidden text-sm leading-relaxed text-foreground/85 sm:block">
              Imagine it. Describe it. Play it.
              <br />
              Your ideas, brought to life by AI.
            </p>
          </div>

          {/* Decorative illustration */}
          <div className="relative hidden h-32 w-[280px] shrink-0 lg:block [@media(max-height:780px)]:lg:hidden" aria-hidden>
            <div className="absolute bottom-2 left-1/2 h-7 w-44 -translate-x-1/2 rounded-[50%] bg-[oklch(0.65_0.25_295)]/40 blur-xl" />
            <div className="absolute bottom-3 left-1/2 h-4 w-40 -translate-x-1/2 rounded-[50%] border border-primary/40 bg-gradient-to-r from-primary/30 via-[oklch(0.65_0.25_295)]/40 to-[oklch(0.82_0.16_195)]/30" />
            <div className="absolute bottom-7 left-1/2 grid size-20 -translate-x-1/2 place-items-center rounded-2xl border border-white/10 bg-gradient-to-br from-[#241b4d] to-[#120c2b] shadow-[0_0_60px_-10px_oklch(0.65_0.25_295)]">
              <Gamepad2 className="size-10 text-[oklch(0.72_0.27_340)]/90" />
            </div>
            <div className="absolute left-2 top-3 grid h-16 w-14 -rotate-6 place-items-center rounded-xl border border-white/10 bg-card/80 shadow-card">
              <Code2 className="size-6 text-primary/80" />
            </div>
            <div className="absolute right-2 top-4 grid h-16 w-16 rotate-6 place-items-center rounded-xl border border-white/10 bg-card/80 shadow-card">
              <ImageIcon className="size-6 text-[oklch(0.82_0.16_195)]/80" />
            </div>
            <div className="absolute right-16 top-0 size-2 rounded-full bg-primary/70" />
            <div className="absolute left-24 top-2 size-1.5 rounded-full bg-[oklch(0.82_0.16_195)]/80" />
          </div>
        </div>
      </div>

      <div className="mx-auto max-w-6xl px-4 py-4 pb-28 sm:px-6 lg:px-10 lg:pb-4 [@media(max-height:780px)]:py-2.5">
        <div className="animate-float-up overflow-hidden rounded-3xl border border-border/60 bg-card shadow-card">
          {/* Card header */}
          <div className="flex items-center justify-between border-b border-border/50 px-4 py-3 sm:px-6">
            <span className="flex items-center gap-3">
              <span className="grid size-9 place-items-center rounded-xl bg-primary/15 text-primary">
                <MessageSquareMore className="size-5" />
              </span>
              <span className="label-mono text-xs tracking-[0.18em] text-primary">CREATE WITH CHAT</span>
            </span>
            <span className="flex items-center gap-2 rounded-full border border-border/60 bg-background/40 px-3 py-1.5 text-xs text-muted-foreground">
              <span className="relative flex size-2">
                <span className="absolute inline-flex size-full animate-ping rounded-full bg-emerald-400 opacity-60" />
                <span className="relative inline-flex size-2 rounded-full bg-emerald-400" />
              </span>
              AI Game Studio
            </span>
          </div>

          <div className="p-3 sm:p-4">
            <div className="flex min-h-[210px] flex-col rounded-2xl [@media(max-height:780px)]:min-h-[170px] border border-border/50 bg-background/50 p-4">
              {freshChat ? (
                /* Welcome state */
                <div className="flex flex-1 flex-col items-center justify-center gap-3 py-2 text-center">
                  <div className="grid size-12 place-items-center rounded-full bg-gradient-to-br from-[oklch(0.72_0.27_340)]/30 to-[oklch(0.65_0.25_295)]/30 ring-1 ring-primary/30">
                    <Bot className="size-6 text-primary" />
                  </div>
                  <div>
                    <h2 className="font-display text-xl font-black">Hey there! 👋</h2>
                    <p className="mt-1 text-xs text-muted-foreground sm:text-sm">What kind of game do you want to create?</p>
                  </div>
                  <div className="flex w-full max-w-3xl flex-wrap items-stretch justify-center gap-2">
                    {ideas.map((idea) => (
                      <button
                        key={idea.text}
                        onClick={() => sendChat(idea.text)}
                        className="flex w-full items-center gap-2.5 rounded-xl border border-border/70 bg-card/60 px-3 py-2 text-left text-xs text-foreground/90 transition hover:border-primary hover:bg-primary/5 sm:w-auto sm:max-w-[230px] sm:text-[13px]"
                      >
                        <span className="grid size-8 shrink-0 place-items-center rounded-lg bg-secondary/70 text-base">
                          {idea.icon}
                        </span>
                        {idea.text}
                      </button>
                    ))}
                  </div>
                </div>
              ) : (
                <>
                  <div className="max-h-[36vh] flex-1 space-y-4 overflow-auto pr-1">
                    {messages.map((message, index) => (
                      <div
                        key={`${message.role}-${index}`}
                        className={`flex ${message.role === "user" ? "justify-end" : "justify-start"}`}
                      >
                        <div
                          className={`max-w-[85%] rounded-2xl px-4 py-2.5 text-sm leading-relaxed sm:max-w-[78%] ${
                            message.role === "user"
                              ? "bg-secondary text-foreground"
                              : "bg-transparent text-foreground"
                          }`}
                        >
                          {message.text}
                        </div>
                      </div>
                    ))}
                  </div>
                  <div className="mt-4 flex flex-wrap gap-2.5 border-t border-border/50 pt-4">
                    {(chatStage === "game" ? ideas.map((i) => i.text) : vibeIdeas).map((idea) => (
                      <button
                        key={idea}
                        onClick={() => sendChat(idea)}
                        className="rounded-full border border-border/70 px-4 py-2 text-xs text-muted-foreground transition-colors hover:border-primary hover:text-foreground"
                      >
                        {idea}
                      </button>
                    ))}
                    {chatStage === "concept" && (
                      <button
                        onClick={() => sendChat("Ok, create it!")}
                        className="rounded-full bg-primary px-4 py-2 text-xs font-bold text-primary-foreground"
                      >
                        Ok, create it!
                      </button>
                    )}
                  </div>
                </>
              )}

              {/* Composer */}
              <div className="mt-3 flex items-center gap-2 rounded-2xl border border-border/60 bg-secondary/50 p-2 pl-4">
                <input
                  value={chatInput}
                  onChange={(e) => setChatInput(e.target.value)}
                  onKeyDown={(e) => {
                    if (e.key === "Enter") sendChat();
                  }}
                  placeholder={chatStage === "game" ? "Type soccer, runner, racing..." : "Type a vibe or confirmation..."}
                  className="min-w-0 flex-1 bg-transparent text-sm outline-none placeholder:text-muted-foreground sm:text-base"
                />
                <button
                  type="button"
                  onClick={() => sendChat()}
                  disabled={phase === "building"}
                  className="grid size-9 shrink-0 place-items-center rounded-full bg-primary text-primary-foreground transition hover:opacity-90 disabled:opacity-60 sm:size-10"
                >
                  <Send className="size-4" />
                </button>
              </div>
            </div>

            {/* Mode cards */}
            <div className="mx-auto mt-3 grid w-full max-w-3xl gap-2.5 sm:grid-cols-2">
              <button
                onClick={() => build("hybrid")}
                disabled={phase === "building"}
                className="group flex items-center gap-3 rounded-2xl border border-indigo-400/20 bg-gradient-to-br from-[#2b2670] to-[#171238] p-3.5 text-left transition hover:border-indigo-300/40 disabled:opacity-60"
              >
                <span className="grid size-10 shrink-0 place-items-center rounded-xl bg-white/10">
                  <Rocket className="size-5 text-sky-300" />
                </span>
                <span className="min-w-0 flex-1">
                  <span className="label-mono block text-xs font-black tracking-[0.14em] text-white">HYBRID MODE</span>
                  <span className="mt-0.5 block text-[11px] leading-relaxed text-white/65">
                    {buildingStrategy === "hybrid" ? "Building your game…" : "Best of both worlds. AI designs, you refine."}
                  </span>
                </span>
                <span className="grid size-8 shrink-0 place-items-center rounded-lg bg-white/10 text-white transition group-hover:bg-white/20">
                  {buildingStrategy === "hybrid" ? <Loader2 className="size-4 animate-spin" /> : <ArrowRight className="size-4" />}
                </span>
              </button>

              <button
                onClick={() => build("pure-agent")}
                disabled={phase === "building"}
                className="group flex items-center gap-3 rounded-2xl border border-pink-400/25 bg-gradient-to-br from-[#86158f] to-[#3b0444] p-3.5 text-left transition hover:border-pink-300/50 disabled:opacity-60"
              >
                <span className="grid size-10 shrink-0 place-items-center rounded-xl bg-white/10">
                  <BriefcaseBusiness className="size-5 text-pink-300" />
                </span>
                <span className="min-w-0 flex-1">
                  <span className="label-mono block text-xs font-black tracking-[0.14em] text-white">PURE AGENT STRATEGY</span>
                  <span className="mt-0.5 block text-[11px] leading-relaxed text-white/65">
                    {buildingStrategy === "pure-agent" ? "Building your game…" : "Let the AI agent handle everything from idea to game."}
                  </span>
                </span>
                <span className="grid size-8 shrink-0 place-items-center rounded-lg bg-white/10 text-white transition group-hover:bg-white/20">
                  {buildingStrategy === "pure-agent" ? <Loader2 className="size-4 animate-spin" /> : <ArrowRight className="size-4" />}
                </span>
              </button>
            </div>
          </div>
        </div>

        {phase !== "idle" && (
          <div className="animate-float-up mt-6 rounded-2xl border border-border/60 bg-card p-6 shadow-card">
            <div className="flex items-start justify-between gap-3">
              <div>
                <h3 className="font-display text-lg font-bold">
                  Build Console
                  {builtGame?.title ? <span className="text-muted-foreground"> · {builtGame.title}</span> : null}
                </h3>
                <p className="label-mono mt-1 text-[10px] text-muted-foreground">
                  {activeBuild?.statusText ?? studio.agentStatus}
                  {phase === "building" ? ` · ${Math.floor(elapsedSec / 60)}m ${elapsedSec % 60}s` : ""}
                </p>
              </div>
              {phase === "building" ? (
                <button
                  onClick={cancelBuild}
                  className="shrink-0 rounded-lg border border-destructive/50 px-3 py-1.5 text-[11px] font-bold uppercase tracking-wider text-destructive transition hover:bg-destructive/10"
                >
                  Cancel build
                </button>
              ) : (
                <button
                  onClick={cancelBuild}
                  className="shrink-0 rounded-lg border border-border/70 px-3 py-1.5 text-[11px] font-bold uppercase tracking-wider text-muted-foreground transition hover:bg-secondary"
                >
                  Dismiss
                </button>
              )}
            </div>
            <ul className="mt-4 space-y-3">
              {steps.map((s, i) => {
                const active = phase === "building" && i === step;
                const complete = phase === "done" || phase === "failed" || i < step;
                return (
                  <li key={s} className="flex items-center gap-3 text-sm">
                    <span className="flex size-6 items-center justify-center rounded-full border border-border/70">
                      {complete ? (
                        <Check className="size-3.5 text-[oklch(0.85_0.2_150)]" />
                      ) : active ? (
                        <Loader2 className="size-3.5 animate-spin text-primary" />
                      ) : (
                        <span className="size-1.5 rounded-full bg-muted-foreground" />
                      )}
                    </span>
                    <span className={complete || active ? "text-foreground" : "text-muted-foreground"}>{s}</span>
                  </li>
                );
              })}
            </ul>

            {phase === "failed" && (
              <div className="mt-6 rounded-xl border border-destructive/40 bg-destructive/10 p-4 text-sm text-destructive">
                {activeBuild?.statusText ?? "The AI build failed."}
                {builtGame?.id ? " The playable template version is still in My Creations." : null}
              </div>
            )}

            {phase === "done" && (
              <div className="mt-6 rounded-xl border border-primary/40 bg-gradient-to-br from-[oklch(0.72_0.27_340)] to-[oklch(0.65_0.25_295)] p-5">
                <p className="label-mono text-[10px] text-white/80">{activeBuild?.statusText ?? studio.status}</p>
                <h4 className="mt-1 font-display text-xl font-black text-white">
                  {builtGame?.title ?? studio.generatedPackage.title} 🎮
                </h4>
                <div className="mt-4 flex flex-wrap gap-3">
                  <button
                    onClick={() =>
                      navigate({
                        to: "/edit/$gameId",
                        params: { gameId: builtGame?.id ?? studio.generatedPackage.id ?? "latest" },
                      })
                    }
                    className="flex items-center gap-2 rounded-lg bg-black/40 px-4 py-2 text-xs font-bold uppercase tracking-wider text-white"
                  >
                    <Wand2 className="size-4" /> Edit Game
                  </button>
                  <button className="flex items-center gap-2 rounded-lg bg-white px-4 py-2 text-xs font-bold uppercase tracking-wider text-black">
                    <Rocket className="size-4" /> Publish
                  </button>
                  <button
                    onClick={() =>
                      navigate({
                        to: "/play/$gameId",
                        params: {
                          gameId:
                            builtGame?.id ?? builtGame?.templateId ?? studio.generatedPackage.templateId ?? "flappy",
                        },
                      })
                    }
                    className="flex items-center gap-2 rounded-lg bg-white px-4 py-2 text-xs font-bold uppercase tracking-wider text-black"
                  >
                    <Play className="size-4" /> Play
                  </button>
                </div>
              </div>
            )}
          </div>
        )}
      </div>
    </div>
  );
}
